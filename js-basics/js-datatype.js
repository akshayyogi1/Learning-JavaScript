console.log("**Data Types in JavaScript**");

// Numbers (integers and decimals)
let age = 30;
let pi = 3.14159;
console.log("Number (integer):", age);
console.log("Number (decimal):", pi);

// Strings (text data)
let Name = "Akshay";
let message = 'This is a string with single quotes.';
console.log("String:", Name);
console.log("String (with quotes):", message);

// Booleans (true or false)
let isLoggedIn = true;
let isNight = false;
console.log("Boolean (true):", isLoggedIn);
console.log("Boolean (false):", isNight);

// Undefined (variable declared but not assigned)
let unassignedVariable;
console.log("Undefined:", unassignedVariable);

// Null (intentionally empty value)
let emptyValue = null;
console.log("Null:", emptyValue);

// Objects (collections of key-value pairs)
let person = {
  firstName: "John",
  lastName: "Doe",
  age: 25
};
console.log("Object:", person);

// Arrays (ordered lists of values)
let fruits = ["apple", "banana", "orange"];
console.log("Array:", fruits);

// Numbers: Represent numeric values, both integers (whole numbers) and decimals.
// Strings: Represent text data enclosed in single or double quotes.
// Booleans: Represent logical values, either true or false.
// Undefined: Represents a variable declared but not assigned a value.
// Null: Represents an intentionally empty value.
// Objects: Complex data structures that store collections of key-value pairs.
// Arrays: Ordered lists of values that can hold different data types.